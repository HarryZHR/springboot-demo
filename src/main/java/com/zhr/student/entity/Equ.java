package com.zhr.student.entity;

public class Equ {
    private String hesId;
    private String equName;
    private String equModel;
    private String equFactory;
    private String first;
    private String second;
    private String third;
    private String equId;
    private String cateId;

    public String getHesId() {
        return hesId;
    }

    public void setHesId(String hesId) {
        this.hesId = hesId;
    }

    public String getEquName() {
        return equName;
    }

    public void setEquName(String equName) {
        this.equName = equName;
    }

    public String getEquModel() {
        return equModel;
    }

    public void setEquModel(String equModel) {
        this.equModel = equModel;
    }

    public String getEquFactory() {
        return equFactory;
    }

    public void setEquFactory(String equFactory) {
        this.equFactory = equFactory;
    }

    public String getFirst() {
        return first;
    }

    public void setFirst(String first) {
        this.first = first;
    }

    public String getSecond() {
        return second;
    }

    public void setSecond(String second) {
        this.second = second;
    }

    public String getThird() {
        return third;
    }

    public void setThird(String third) {
        this.third = third;
    }

    public String getEquId() {
        return equId;
    }

    public void setEquId(String equId) {
        this.equId = equId;
    }

    public String getCateId() {
        return cateId;
    }

    public void setCateId(String cateId) {
        this.cateId = cateId;
    }
}
